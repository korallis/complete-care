export async function recordStockTransaction(
  organisationId: string,
  _userId: string,
  data: unknown,
) {
  const parsed = createStockTransactionSchema.parse(data);
  const { orgId, userId } = await requirePermission('create', 'medications');
  assertBelongsToOrg(organisationId, orgId);

  const stock = await loadMedicationStock(parsed.medicationStockId);
  if (!stock) {
    throw new Error('Medication stock not found');
  }

  assertBelongsToOrg(stock.organisationId, orgId);

  if (stock.isControlledDrug) {
    if (!parsed.witnessedById) {
      throw new Error('Witness is required for controlled drug transactions');
    }

    if (parsed.witnessedById === userId) {
      throw new Error('Witness must be a different staff member from the person performing the operation');
    }

    const witnessInOrg = await ensureActiveMemberInOrg(parsed.witnessedById, orgId);
    if (!witnessInOrg) {
      throw new Error('Witness must be an active member of this organisation');
    }
  }

  let nextBatchQuantity: number | null = null;
  if (parsed.stockBatchId) {
    const batch = await loadStockBatch(parsed.stockBatchId);
    if (!batch) {
      throw new Error('Stock batch not found');
    }

    assertBelongsToOrg(batch.organisationId, orgId);
    if (batch.medicationStockId !== stock.id) {
      throw new Error('Stock batch does not belong to the selected medication stock');
    }

    nextBatchQuantity = batch.quantity + parsed.quantity;
    if (nextBatchQuantity < 0) {
      throw new Error('Insufficient stock in the selected batch');
    }

    await db
      .update(stockBatches)
      .set({
        quantity: nextBatchQuantity,
        isExhausted: nextBatchQuantity === 0,
        updatedAt: new Date(),
      })
      .where(eq(stockBatches.id, batch.id));
  }

  const nextQuantity = stock.currentQuantity + parsed.quantity;
  if (nextQuantity < 0) {
    throw new Error('Insufficient stock available for this transaction');
  }

  await db
    .update(medicationStock)
    .set({
      currentQuantity: nextQuantity,
      updatedAt: new Date(),
    })
    .where(eq(medicationStock.id, stock.id));

  const [transaction] = await db
    .insert(stockTransactions)
    .values({
      organisationId: orgId,
      medicationStockId: stock.id,
      stockBatchId: parsed.stockBatchId ?? null,
      transactionType: parsed.transactionType,
      quantity: parsed.quantity,
      balanceAfter: nextQuantity,
      performedById: userId,
      witnessedById: parsed.witnessedById ?? null,
      reason: parsed.reason ?? null,
      notes: parsed.notes ?? null,
      administrationRecordId: parsed.administrationRecordId ?? null,
      sourceDestination: parsed.sourceDestination ?? null,
    })
    .returning();

  const updatedStock = {
    ...stock,
    currentQuantity: nextQuantity,
  };

  await createAutoReorderIfNeeded(updatedStock, userId);

  await auditLog(
    'create',
    'stock_transaction',
    transaction.id,
    {
      before: { currentQuantity: stock.currentQuantity },
      after: {
        currentQuantity: nextQuantity,
        quantity: parsed.quantity,
        transactionType: parsed.transactionType,
        stockBatchId: parsed.stockBatchId ?? null,
      },
    },
    { userId, organisationId: orgId },
  );

  return { success: true, data: transaction };
}

/**
 * Check stock levels and auto-generate reorder requests where needed.
 * Called after each stock transaction or on a schedule.
 */
export async function checkAndGenerateReorders(organisationId: string, _userId: string) {
  const { orgId, userId } = await requirePermission('update', 'medications');
  assertBelongsToOrg(organisationId, orgId);

  const rows = await db
    .select()
    .from(medicationStock)
    .where(eq(medicationStock.organisationId, orgId))
    .orderBy(asc(medicationStock.medicationName));

  let reordersGenerated = 0;

  for (const stock of rows) {
    const reorder = await createAutoReorderIfNeeded(stock, userId);
    if (reorder) {
      reordersGenerated += 1;
    }
  }

  return { success: true, reordersGenerated };
}

