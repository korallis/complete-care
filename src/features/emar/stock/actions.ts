export async function updateReorderStatus(
  organisationId: string,
  reorderId: string,
  _userId: string,
  data: unknown,
) {
  const parsed = updateReorderStatusSchema.parse(data);
  const { orgId, userId } = await requirePermission('update', 'medications');
  assertBelongsToOrg(organisationId, orgId);

  const existing = await loadReorderRequest(reorderId);
  if (!existing) {
    throw new Error('Reorder request not found');
  }

  assertBelongsToOrg(existing.organisationId, orgId);

  if (parsed.status === 'cancelled' && !parsed.cancellationReason) {
    throw new Error('Cancellation reason is required when cancelling a reorder request');
  }

  const updatePayload: Record<string, unknown> = {
    status: parsed.status,
    quantityReceived: parsed.quantityReceived ?? existing.quantityReceived,
    pharmacyReference: parsed.pharmacyReference ?? existing.pharmacyReference,
    expectedDeliveryDate: parsed.expectedDeliveryDate ?? existing.expectedDeliveryDate,
    cancellationReason: parsed.cancellationReason ?? existing.cancellationReason,
    notes: parsed.notes ?? existing.notes,
    updatedAt: new Date(),
  };

  if (parsed.status === 'approved') {
    updatePayload.approvedById = userId;
    updatePayload.approvedAt = new Date();
  }

  if (parsed.status === 'received' || parsed.status === 'partially_received') {
    updatePayload.receivedById = userId;
    updatePayload.receivedAt = new Date();

    if ((parsed.quantityReceived ?? 0) > 0) {
      const stock = await loadMedicationStock(existing.medicationStockId);
      if (!stock) {
        throw new Error('Medication stock not found');
      }

      assertBelongsToOrg(stock.organisationId, orgId);

      const nextQuantity = stock.currentQuantity + (parsed.quantityReceived ?? 0);

      await db
        .update(medicationStock)
        .set({
          currentQuantity: nextQuantity,
          updatedAt: new Date(),
        })
        .where(eq(medicationStock.id, stock.id));

      await db
        .insert(stockTransactions)
        .values({
          organisationId: orgId,
          medicationStockId: stock.id,
          transactionType: 'receipt',
          quantity: parsed.quantityReceived ?? 0,
          balanceAfter: nextQuantity,
          performedById: userId,
          reorderRequestId: existing.id,
          reason: `Receipt for reorder ${existing.id}`,
          sourceDestination: parsed.pharmacyReference ?? 'pharmacy',
        })
        .returning();
    }
  }

  const [updated] = await db
    .update(reorderRequests)
    .set(updatePayload)
    .where(eq(reorderRequests.id, reorderId))
    .returning();

  await auditLog(
    'update',
    'reorder_request',
    updated.id,
    {
      before: {
        status: existing.status,
        quantityReceived: existing.quantityReceived,
      },
      after: {
        status: updated.status,
        quantityReceived: updated.quantityReceived,
      },
    },
    { userId, organisationId: orgId },
  );

  return { success: true, data: updated };
}

