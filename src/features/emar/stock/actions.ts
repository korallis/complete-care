export async function createStockBatch(
  organisationId: string,
  _userId: string,
  data: unknown,
) {
  const parsed = createStockBatchSchema.parse(data);
  const { orgId, userId } = await requirePermission('update', 'medications');
  assertBelongsToOrg(organisationId, orgId);

  const stock = await loadMedicationStock(parsed.medicationStockId);
  if (!stock) {
    throw new Error('Medication stock not found');
  }

  assertBelongsToOrg(stock.organisationId, orgId);

  const nextQuantity = stock.currentQuantity + parsed.quantity;

  const [batch] = await db
    .insert(stockBatches)
    .values({
      organisationId: orgId,
      medicationStockId: stock.id,
      batchNumber: parsed.batchNumber,
      expiryDate: parsed.expiryDate,
      quantity: parsed.quantity,
      originalQuantity: parsed.quantity,
      expiryAlertDays: parsed.expiryAlertDays,
    })
    .returning();

  await db
    .update(medicationStock)
    .set({
      currentQuantity: nextQuantity,
      updatedAt: new Date(),
    })
    .where(eq(medicationStock.id, stock.id));

  const [transaction] = await db
    .insert(stockTransactions)
    .values({
      organisationId: orgId,
      medicationStockId: stock.id,
      stockBatchId: batch.id,
      transactionType: 'receipt',
      quantity: parsed.quantity,
      balanceAfter: nextQuantity,
      performedById: userId,
      reason: 'Batch receipt recorded',
      sourceDestination: stock.pharmacySupplier ?? 'pharmacy',
    })
    .returning();

  await auditLog(
    'create',
    'stock_batch',
    batch.id,
    {
      before: null,
      after: {
        medicationStockId: batch.medicationStockId,
        batchNumber: batch.batchNumber,
        quantity: batch.quantity,
        expiryDate: batch.expiryDate,
      },
    },
    { userId, organisationId: orgId },
  );

  await auditLog(
    'create',
    'stock_transaction',
    transaction.id,
    {
      before: { currentQuantity: stock.currentQuantity },
      after: { currentQuantity: nextQuantity, transactionType: 'receipt' },
    },
    { userId, organisationId: orgId },
  );

  return { success: true, data: batch };
}

/**
 * Get batches approaching expiry for an organisation.
 * Returns batches where expiry date is within the configured alert window.
 */
export async function getExpiryAlerts(
  organisationId: string,
): Promise<ExpiryAlert[]> {
  const { orgId } = await requirePermission('read', 'medications');
  assertBelongsToOrg(organisationId, orgId);

  const rows = await db
    .select({
      batchId: stockBatches.id,
      medicationStockId: stockBatches.medicationStockId,
      medicationName: medicationStock.medicationName,
      batchNumber: stockBatches.batchNumber,
      expiryDate: stockBatches.expiryDate,
      quantity: stockBatches.quantity,
      expiryAlertDays: stockBatches.expiryAlertDays,
      isExhausted: stockBatches.isExhausted,
      expiryAlertAcknowledged: stockBatches.expiryAlertAcknowledged,
    })
    .from(stockBatches)
    .innerJoin(medicationStock, eq(stockBatches.medicationStockId, medicationStock.id))
    .where(eq(stockBatches.organisationId, orgId))
    .orderBy(asc(stockBatches.expiryDate));

  const today = new Date();
  const todayUtc = new Date(Date.UTC(
    today.getUTCFullYear(),
    today.getUTCMonth(),
    today.getUTCDate(),
  ));

  return rows.flatMap((row) => {
    if (row.isExhausted) {
      return [];
    }

    const expiryDate = toDateOnly(row.expiryDate);
    const daysUntilExpiry = Math.ceil(
      (expiryDate.getTime() - todayUtc.getTime()) / 86_400_000,
    );

    if (daysUntilExpiry > row.expiryAlertDays) {
      return [];
    }

    return [{
      batchId: row.batchId,
      medicationStockId: row.medicationStockId,
      medicationName: row.medicationName,
      batchNumber: row.batchNumber,
      expiryDate: toDateOnlyString(row.expiryDate),
      daysUntilExpiry,
      quantity: row.quantity,
      isExpired: daysUntilExpiry < 0,
      isAcknowledged: row.expiryAlertAcknowledged,
    }];
  });
}

export async function acknowledgeExpiryAlert(
  organisationId: string,
  batchId: string,
  _userId: string,
) {
  const { orgId, userId } = await requirePermission('update', 'medications');
  assertBelongsToOrg(organisationId, orgId);

  const batch = await loadStockBatch(batchId);
  if (!batch) {
    throw new Error('Stock batch not found');
  }

  assertBelongsToOrg(batch.organisationId, orgId);

  await db
    .update(stockBatches)
    .set({
      expiryAlertAcknowledged: true,
      expiryAlertAcknowledgedById: userId,
      expiryAlertAcknowledgedAt: new Date(),
      updatedAt: new Date(),
    })
    .where(eq(stockBatches.id, batchId));

  await auditLog(
    'update',
    'stock_batch',
    batchId,
    {
      before: { expiryAlertAcknowledged: batch.expiryAlertAcknowledged },
      after: { expiryAlertAcknowledged: true },
    },
    { userId, organisationId: orgId },
  );

  return { success: true };
}

