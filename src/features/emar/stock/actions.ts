export async function signHandoverReport(
  organisationId: string,
  reportId: string,
  _userId: string,
  role: 'outgoing' | 'incoming',
) {
  const { orgId, userId } = await requirePermission('update', 'medications');
  assertBelongsToOrg(organisationId, orgId);

  const report = await loadHandoverReport(reportId);
  if (!report) {
    throw new Error('Handover report not found');
  }

  assertBelongsToOrg(report.organisationId, orgId);

  const now = new Date();
  const updatePayload: Record<string, unknown> = {
    updatedAt: now,
  };

  if (role === 'outgoing') {
    updatePayload.outgoingStaffId = userId;
    updatePayload.outgoingSignedAt = now;
  } else {
    updatePayload.incomingStaffId = userId;
    updatePayload.incomingSignedAt = now;
  }

  const nextOutgoing = role === 'outgoing' ? userId : report.outgoingStaffId;
  const nextIncoming = role === 'incoming' ? userId : report.incomingStaffId;
  updatePayload.isCompleted = Boolean(nextOutgoing && nextIncoming);

  await db
    .update(handoverReports)
    .set(updatePayload)
    .where(eq(handoverReports.id, reportId));

  await auditLog(
    'sign',
    'handover_report',
    reportId,
    {
      before: {
        outgoingStaffId: report.outgoingStaffId,
        incomingStaffId: report.incomingStaffId,
        isCompleted: report.isCompleted,
      },
      after: {
        outgoingStaffId: role === 'outgoing' ? userId : report.outgoingStaffId,
        incomingStaffId: role === 'incoming' ? userId : report.incomingStaffId,
        isCompleted: Boolean(nextOutgoing && nextIncoming),
      },
    },
    { userId, organisationId: orgId },
  );

  return { success: true };
}
