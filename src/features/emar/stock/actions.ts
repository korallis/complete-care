async function hasOpenReorderRequest(organisationId: string, medicationStockId: string) {
  const rows = await db
    .select({ id: reorderRequests.id, status: reorderRequests.status })
    .from(reorderRequests)
    .where(
      and(
        eq(reorderRequests.organisationId, organisationId),
        eq(reorderRequests.medicationStockId, medicationStockId),
      ),
    )
    .orderBy(asc(reorderRequests.createdAt));

  return rows.some((row) => OPEN_REORDER_STATUSES.has(row.status));
}

async function createAutoReorderIfNeeded(stock: typeof medicationStock.$inferSelect, requestedById: string) {
  if (stock.reorderQuantity <= 0 || stock.currentQuantity > stock.reorderPoint) {
    return null;
  }

  const hasOpen = await hasOpenReorderRequest(stock.organisationId, stock.id);
  if (hasOpen) {
    return null;
  }

  const [reorder] = await db
    .insert(reorderRequests)
    .values({
      organisationId: stock.organisationId,
      medicationStockId: stock.id,
      quantityRequested: stock.reorderQuantity,
      status: 'pending',
      isAutoGenerated: true,
      requestedById,
      notes: 'Auto-generated because stock reached the reorder threshold.',
    })
    .returning();

  await auditLog(
    'create',
    'reorder_request',
    reorder.id,
    {
      before: null,
      after: {
        medicationStockId: stock.id,
        quantityRequested: reorder.quantityRequested,
        status: reorder.status,
        isAutoGenerated: true,
      },
    },
    { userId: requestedById, organisationId: stock.organisationId },
  );

  return reorder;
}

