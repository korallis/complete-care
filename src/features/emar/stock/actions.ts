const OPEN_REORDER_STATUSES = new Set([
  'pending',
  'approved',
  'ordered',
  'partially_received',
]);

function toDateOnlyString(value: string | Date): string {
  if (typeof value === 'string') {
    return value;
  }

  return value.toISOString().slice(0, 10);
}

function toDateOnly(value: string | Date): Date {
  const raw = typeof value === 'string' ? value : toDateOnlyString(value);
  return new Date(`${raw}T00:00:00.000Z`);
}

async function ensureActiveMemberInOrg(userId: string, organisationId: string): Promise<boolean> {
  const rows = await db
    .select({ id: memberships.id })
    .from(memberships)
    .where(
      and(
        eq(memberships.userId, userId),
        eq(memberships.organisationId, organisationId),
        eq(memberships.status, 'active'),
      ),
    )
    .limit(1);

  return rows.length > 0;
}

