export async function createMedicationStock(
  organisationId: string,
  _userId: string,
  data: unknown,
) {
  const parsed = createMedicationStockSchema.parse(data);

  if (parsed.isControlledDrug && !parsed.controlledDrugSchedule) {
    throw new Error('Controlled drug schedule is required for controlled drugs');
  }

  const { orgId, userId } = await requirePermission('create', 'medications');
  assertBelongsToOrg(organisationId, orgId);

  const [stock] = await db
    .insert(medicationStock)
    .values({
      organisationId: orgId,
      medicationName: parsed.medicationName,
      medicationCode: parsed.medicationCode ?? null,
      form: parsed.form,
      strength: parsed.strength,
      currentQuantity: parsed.currentQuantity,
      minimumThreshold: parsed.minimumThreshold,
      reorderPoint: parsed.reorderPoint,
      reorderQuantity: parsed.reorderQuantity,
      unit: parsed.unit,
      storageRequirement: parsed.storageRequirement,
      isControlledDrug: parsed.isControlledDrug,
      controlledDrugSchedule: parsed.controlledDrugSchedule ?? null,
      pharmacySupplier: parsed.pharmacySupplier ?? null,
    })
    .returning();

  await auditLog(
    'create',
    'medication_stock',
    stock.id,
    {
      before: null,
      after: {
        medicationName: stock.medicationName,
        form: stock.form,
        strength: stock.strength,
        currentQuantity: stock.currentQuantity,
        reorderPoint: stock.reorderPoint,
        isControlledDrug: stock.isControlledDrug,
      },
    },
    { userId, organisationId: orgId },
  );

  return { success: true, data: stock };
}

export async function updateMedicationStock(
  organisationId: string,
  stockId: string,
  _userId: string,
  data: unknown,
) {
  const parsed = updateMedicationStockSchema.parse(data);
  const { orgId, userId } = await requirePermission('update', 'medications');
  assertBelongsToOrg(organisationId, orgId);

  const existing = await loadMedicationStock(stockId);
  if (!existing) {
    throw new Error('Medication stock not found');
  }

  assertBelongsToOrg(existing.organisationId, orgId);

  const nextIsControlledDrug = parsed.isControlledDrug ?? existing.isControlledDrug;
  const nextSchedule = parsed.controlledDrugSchedule ?? existing.controlledDrugSchedule;

  if (nextIsControlledDrug && !nextSchedule) {
    throw new Error('Controlled drug schedule is required for controlled drugs');
  }

  const [updated] = await db
    .update(medicationStock)
    .set({
      ...parsed,
      controlledDrugSchedule: nextSchedule ?? null,
      medicationCode: parsed.medicationCode ?? existing.medicationCode,
      pharmacySupplier: parsed.pharmacySupplier ?? existing.pharmacySupplier,
      updatedAt: new Date(),
    })
    .where(eq(medicationStock.id, stockId))
    .returning();

  await auditLog(
    'update',
    'medication_stock',
    updated.id,
    {
      before: {
        currentQuantity: existing.currentQuantity,
        reorderPoint: existing.reorderPoint,
        isControlledDrug: existing.isControlledDrug,
      },
      after: {
        currentQuantity: updated.currentQuantity,
        reorderPoint: updated.reorderPoint,
        isControlledDrug: updated.isControlledDrug,
      },
    },
    { userId, organisationId: orgId },
  );

  return { success: true, data: updated };
}

